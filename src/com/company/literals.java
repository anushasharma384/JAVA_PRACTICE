package com.company;

public class literals {
    public static void main(String[] args) {
        byte age = 34;
        int i = 12;
        char name = 'a';
        //f or F is compulsory with float as default decimal value goes into double
        float f = 1.2f;
        //d or D isn't compulsory with double but can use this
        double d = 1.22d;
        //l or L is compulsory with long as default integer value goes into integer
        long sum = 5666666666l;
        short s = 2;
        String str = "Anusha";
        System.out.println(age);
        System.out.println(i);
        System.out.println(name);
        System.out.println(f);
        System.out.println(d);
        System.out.println(sum);
        System.out.println(str);
        System.out.println(s);
    }
}
